dupe_counter = {}


def fix_dupe_path(path):
    count = dupe_counter.get(path, 0) + 1
    dupe_counter[path] = count
    suffix = "_%d" % count
    if '.' in path:
        head, sep, tail = path.rpartition('.')
        return head + suffix + sep + tail
    else:
        return path + suffix
